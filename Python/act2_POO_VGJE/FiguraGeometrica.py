class FiguraGeometrica:

    def __init__(self, Area, Perimetro):
        self.Area = Area
        self.Perimetro = Perimetro

    def setArea(self, Area):
        self.Area = Area

    def getArea(self):
        return self.Area

    def setPerimetro(self, Perimetro):
        self.Perimetro = Perimetro

    def getPerimetro(self):
        return self.Perimetro