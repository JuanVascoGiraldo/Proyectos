
package Modelo;


public class MEquipo {
    
    int id, id_labo, id_estado, numero, materia;
    String des_labo, des_materia, des_estado;

    public MEquipo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_labo() {
        return id_labo;
    }

    public void setId_labo(int id_labo) {
        this.id_labo = id_labo;
    }

    public int getId_estado() {
        return id_estado;
    }

    public void setId_estado(int id_estado) {
        this.id_estado = id_estado;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public int getMateria() {
        return materia;
    }

    public void setMateria(int materia) {
        this.materia = materia;
    }

    public String getDes_labo() {
        return des_labo;
    }

    public void setDes_labo(String des_labo) {
        this.des_labo = des_labo;
    }

    public String getDes_materia() {
        return des_materia;
    }

    public void setDes_materia(String des_materia) {
        this.des_materia = des_materia;
    }

    public String getDes_estado() {
        return des_estado;
    }

    public void setDes_estado(String des_estado) {
        this.des_estado = des_estado;
    }
    
    
    
}
