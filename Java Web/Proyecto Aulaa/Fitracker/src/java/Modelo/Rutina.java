package Modelo;

public class Rutina {
    int id_eje;
    int repeticiones;
    public Rutina(){
        
    }

    public int getId_eje() {
        return id_eje;
    }

    public void setId_eje(int id_eje) {
        this.id_eje = id_eje;
    }

    public int getRepeticiones() {
        return repeticiones;
    }

    public void setRepeticiones(int repeticiones) {
        this.repeticiones = repeticiones;
    }
    
}
