
package calculadoraconclases;

public class Operaciones {
    float numero1;
    float numero2;
    float resultado;
    public float sumar(float unnumero, float otronumero){
    numero1= unnumero;
    numero2= otronumero;
    resultado= numero1 + numero2;
    return resultado;
    }
    public float multiplicar(float unnumero, float otronumero){
     numero1= unnumero;
    numero2= otronumero;
    resultado= numero1 * numero2;
    return resultado;
    }
    public float dividir(float unnumero, float otronumero){
     numero1= unnumero;
    numero2= otronumero;
    resultado= numero1 * numero2;
    return resultado;
    }
}
